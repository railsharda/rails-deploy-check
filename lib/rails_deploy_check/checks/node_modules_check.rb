# frozen_string_literal: true

module RailsDeployCheck
  module Checks
    class NodeModulesCheck
      attr_reader :result

      def initialize(app_path: Dir.pwd, check_yarn_integrity: true)
        @app_path = app_path
        @check_yarn_integrity = check_yarn_integrity
        @result = Result.new("NodeModules")
      end

      def run
        check_package_json_exists
        check_node_modules_exists
        check_yarn_lock_or_package_lock
        check_yarn_integrity if @check_yarn_integrity && yarn_lock_exists?
        result
      end

      private

      def check_package_json_exists
        if File.exist?(package_json_path)
          result.add_info("package.json found")
        else
          result.add_info("No package.json found, skipping Node.js checks")
          # Not an error — not all Rails apps use Node
        end
      end

      def check_node_modules_exists
        return unless File.exist?(package_json_path)

        if Dir.exist?(node_modules_path)
          result.add_info("node_modules directory exists")
        else
          result.add_error("node_modules directory missing — run `yarn install` or `npm install`")
        end
      end

      def check_yarn_lock_or_package_lock
        return unless File.exist?(package_json_path)

        if yarn_lock_exists?
          result.add_info("yarn.lock found")
        elsif File.exist?(File.join(@app_path, "package-lock.json"))
          result.add_info("package-lock.json found")
        else
          result.add_warning("No lockfile found (yarn.lock or package-lock.json) — dependency versions may be unpredictable")
        end
      end

      def check_yarn_integrity
        integrity_file = File.join(node_modules_path, ".yarn-integrity")
        if File.exist?(integrity_file)
          result.add_info("yarn integrity file present")
        else
          result.add_warning("yarn.lock exists but node_modules/.yarn-integrity is missing — consider running `yarn install`")
        end
      end

      def package_json_path
        File.join(@app_path, "package.json")
      end

      def node_modules_path
        File.join(@app_path, "node_modules")
      end

      def yarn_lock_exists?
        File.exist?(File.join(@app_path, "yarn.lock"))
      end
    end
  end
end
