# frozen_string_literal: true

require "spec_helper"
require "rails_deploy_check/checks/node_modules_check"

RSpec.describe RailsDeployCheck::Checks::NodeModulesCheck do
  def create_file(path, content = "")
    FileUtils.mkdir_p(File.dirname(path))
    File.write(path, content)
  end

  def build_check(app_path:, check_yarn_integrity: true)
    described_class.new(app_path: app_path, check_yarn_integrity: check_yarn_integrity)
  end

  context "when no package.json exists" do
    it "adds an info message and does not error" do
      with_tmp_rails_app do |app_path|
        result = build_check(app_path: app_path).run
        expect(result.errors).to be_empty
        expect(result.infos).to include(match(/No package.json found/))
      end
    end
  end

  context "when package.json exists" do
    context "and node_modules is missing" do
      it "adds an error about missing node_modules" do
        with_tmp_rails_app do |app_path|
          create_file(File.join(app_path, "package.json"), '{"name":"app"}')
          result = build_check(app_path: app_path).run
          expect(result.errors).to include(match(/node_modules directory missing/))
        end
      end
    end

    context "and node_modules exists" do
      it "adds an info message" do
        with_tmp_rails_app do |app_path|
          create_file(File.join(app_path, "package.json"), '{"name":"app"}')
          FileUtils.mkdir_p(File.join(app_path, "node_modules"))
          result = build_check(app_path: app_path).run
          expect(result.errors).to be_empty
          expect(result.infos).to include(match(/node_modules directory exists/))
        end
      end
    end

    context "with yarn.lock and yarn integrity file" do
      it "reports all green" do
        with_tmp_rails_app do |app_path|
          create_file(File.join(app_path, "package.json"), '{"name":"app"}')
          create_file(File.join(app_path, "yarn.lock"), "")
          FileUtils.mkdir_p(File.join(app_path, "node_modules"))
          create_file(File.join(app_path, "node_modules", ".yarn-integrity"), "{}")
          result = build_check(app_path: app_path).run
          expect(result.errors).to be_empty
          expect(result.warnings).to be_empty
          expect(result.infos).to include(match(/yarn.lock found/))
          expect(result.infos).to include(match(/yarn integrity file present/))
        end
      end
    end

    context "with yarn.lock but missing yarn integrity" do
      it "adds a warning" do
        with_tmp_rails_app do |app_path|
          create_file(File.join(app_path, "package.json"), '{"name":"app"}')
          create_file(File.join(app_path, "yarn.lock"), "")
          FileUtils.mkdir_p(File.join(app_path, "node_modules"))
          result = build_check(app_path: app_path).run
          expect(result.warnings).to include(match(/yarn-integrity is missing/))
        end
      end
    end

    context "with no lockfile" do
      it "adds a warning about missing lockfile" do
        with_tmp_rails_app do |app_path|
          create_file(File.join(app_path, "package.json"), '{"name":"app"}')
          FileUtils.mkdir_p(File.join(app_path, "node_modules"))
          result = build_check(app_path: app_path, check_yarn_integrity: false).run
          expect(result.warnings).to include(match(/No lockfile found/))
        end
      end
    end

    context "with package-lock.json" do
      it "recognizes npm lockfile" do
        with_tmp_rails_app do |app_path|
          create_file(File.join(app_path, "package.json"), '{"name":"app"}')
          create_file(File.join(app_path, "package-lock.json"), "{}")
          FileUtils.mkdir_p(File.join(app_path, "node_modules"))
          result = build_check(app_path: app_path, check_yarn_integrity: false).run
          expect(result.warnings).to be_empty
          expect(result.infos).to include(match(/package-lock.json found/))
        end
      end
    end
  end
end
